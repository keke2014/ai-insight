#!/bin/bash
# ============================================================
# AI洞察 - 极简一键发布脚本 (One-Click Publish)
# 功能：封装了从「模板生成」到「多端构建与一键部署」的全部流程
# ============================================================

set -euo pipefail

# 默认使用今天日期，也支持传参覆盖，如 ./one_click_publish.sh 2026-03-22
DATE="${1:-$(date +"%Y-%m-%d")}"
PROJECT_DIR="$(cd "$(dirname "$0")/.." && pwd)"
cd "$PROJECT_DIR"

echo "=================================================="
echo "🚀 AI 洞察极简一键更新发布 ($DATE)"
echo "=================================================="

# ==========================================
# 步骤 1: 准备输入数据 (顺序执行)
# ==========================================
echo -e "\n[1/2] 准备日常输入数据..."
# 调用 JSON 生成器（如果已存在则跳过并验证，不存在则生成空白模板）
python3 scripts/gen_daily_json.py "$DATE"

JSON_FILE="data/daily-content-${DATE}.json"
echo -e "\n👉 提示：如果还没填写今天的内容，请现在去编辑 ${JSON_FILE}。"
read -p "编辑修改完毕后，请按 [Enter] 键继续执行后续自动部署... " dummy

echo -e "\n=================================================="
# ==========================================
# 步骤 2: 执行高阶编排管线 (包含并行/串行混合步骤)
# ==========================================
echo -e "[2/2] 正在全自动执行生成、构建及并线多端分发..."
echo -e "      (包含: 链接抽检 → 质量门 → HTML生成构建 → GitHub部署推送 → 外部公共版同步脱敏)\n"

# 调用 orchestrator 的 finalize 指令，该指令内部自动完成串行和并行的后续动作
python3 scripts/ai_daily_orchestrator.py finalize --date "$DATE"

echo "=================================================="
echo "🎉 全流程完成！"
echo "🌐 您的最新日报不仅自动构建了搜索库、更新了首页，还一键同步到了 Github Pages 和外部平台。"
